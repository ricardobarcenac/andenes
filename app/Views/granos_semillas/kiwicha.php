          <h2>Descripción</h2>
          <p>Es una planta comúnmente cultivada en las montañas andinas entre los 2,000 y 3,000 metros de altura. Alimento muy valorado desde
          tiempos pre-Incas e Incas en Perú, siendo parte esencial en su dieta diaria.</p>
          <p>Kiwicha o amaranto, es el alimento estrella de los astronautas de la NASA, gracias a su alto contenido de proteínas (oscilando
          entre 13~18%) y sobretodo por su excelente contenido de oligoelementos, como: calcio, fósforo, hierro, potasio y zinc y el
          complejo vitamínico B.</p>
          <p>Su equilibrio proteico hace de la kiwicha una buena fuente de lisina, metionina y triptófano (aminoácidos esenciales para el
          funcionamiento del cuerpo humano). Es un alimento sin saponinas, sin gluten y alto en fibra, siendo una excelente opción para
          celíacos y vegetarianos.</p>
