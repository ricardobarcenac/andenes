          <h2>說明</h2>
          <p>芝麻是人類熟知的籽油之一，因其具營養、預防和治療的特性而被廣泛用於烹飪和傳統藥學， 特別適合兒童和孕婦。</p>
          <p>我們的有機芝麻產自於安第斯山脈的高山上，採用古法製造且遵循有機協議，在沒有任何基因改造的自然環境中生產。</p>
