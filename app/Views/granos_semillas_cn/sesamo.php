        <div class="col-12 col-md-12 col-lg-6 text-justify">
          <h2>Beneficios para la salud</h2>
          <ul>
            <li>富含多種重要的營養物質、礦物質、抗氧化劑、及維生素．</li>
            <li>富含單元不飽和脂肪酸，油酸，其中含有高達50％的脂肪酸​​。油酸有助於降低低密度脂蛋白或“壞膽固醇”，並增加血液中的高密度脂蛋白或“好膽固醇”。</li>
            <li>種子也是膳食蛋白質的重要來源，具有優良氨基酸，特別有助於兒童的生長。</li>
            <li>芝麻籽含有芝麻酚，芝麻素酚等。芝麻酚和芝麻素酚是多酚抗氧化劑。</li>
            <li>芝麻是富含優質維生素和礦物質的種子之一。它們是維他命B群的優良來源，如菸酸，葉酸，維生命B1和維他命B6。</li>
            <li>葉酸對DNA合成至關重要。懷孕婦女食用有益於預防新生兒神經管缺陷。</li>
            <li>菸酸有助於降低血液中的低密度脂蛋白膽固醇濃度。此外它可增強腦部的GABA(γ-氨基丁酸)活性，有助於減少焦慮和神經症狀。</li>
            <li>種子是許多人體必需礦物質的豐富來源。鈣，鐵，錳，鋅，鎂，硒和銅。許多這些礦物質在骨骼礦化、紅血球生成、酶合成、生長激素以及心臟和骨骼肌活動的調節中都扮演重要的角色。</li>
          </ul>
