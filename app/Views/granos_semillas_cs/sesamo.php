          <h2>说明</h2>
          <p>芝麻是人类熟知的籽油之一，因其具营养、预防和治疗的特性而被广泛用于烹饪和传统药学， 特别适合儿童和孕妇。</p>
          <p>我们的有机芝麻产自于安第斯山脉的高山上，采用古法制造且遵循有机协议，在没有任何基因改造的自然环境中生产。</p>
        </div>
        <div class="col-12 col-md-12 col-lg-6 text-justify">
          <h2>益处</h2>
          <ul>
            <li>富含多种重要的营养物质、矿物质、抗氧化剂、及维生素．</li>
            <li>富含单元不饱和脂肪酸，油酸，其中含有高达50％的脂肪酸​​。油酸有助于降低低密度脂蛋白或“坏胆固醇”，并增加血液中的高密度脂蛋白或“好胆固醇”。</li>
            <li>种子也是膳食蛋白质的重要来源，具有优良氨基酸，特别有助于儿童的生长。</li>
            <li>芝麻籽含有芝麻酚，芝麻素酚等。芝麻酚和芝麻素酚是多酚抗氧化剂。</li>
            <li>芝麻是富含优质维生素和矿物质的种子之一。它们是维他命B群的优良来源，如烟酸，叶酸，维生命B1和维他命B6。</li>
            <li>叶酸对DNA合成至关重要。怀孕妇女食用有益于预防新生儿神经管缺陷。</li>
            <li>烟酸有助于降低血液中的低密度脂蛋白胆固醇浓度。此外它可增强脑部的GABA(γ-氨基丁酸)活性，有助于减少焦虑和神经症状。</li>
            <li>种子是许多人体必需矿物质的丰富来源。钙，铁，锰，锌，镁，硒和铜。许多这些矿物质在骨骼矿化、红血球生成、酶合成、生长激素以及心脏和骨骼肌活动的调节中都扮演重要的角色。</li>
          </ul>
